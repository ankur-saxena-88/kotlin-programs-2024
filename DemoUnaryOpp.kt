/*
Kotlin - Unary operator demo program
Where, num = 10
*/

fun main(){

    var num = 10

    println("First print then increment: ${num++}")
    println("First increment then print: ${++num}")
    println("First print then decrement: ${num--}")
    println("First decrement then print: ${--num}")
}