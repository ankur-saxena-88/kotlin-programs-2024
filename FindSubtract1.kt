/*
Write a Kotlin program to find the subtraction of two numbers.
Where, num1 = 50 and num2 = 15
*/

fun main(){

    val num1 = 50
    val num2 = 15

    // printing value
    println("First number is: $num1")
    println("Second number is: $num2")

    // calculate subtraction
    val result = num1 - num2

    // printing result
    println("The subtraction of $num1 and $num2 is :: $result")
}