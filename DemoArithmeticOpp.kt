/*
Kotlin - Arithmetic operators demo program
where, first var = 20 and second var = 4
*/

fun main(){
    
    var a = 20 
    var b = 4 
    
    println("a + b = ${(a + b)}")
    println("a - b = ${(a - b)}")
    println("a * b = ${(a * b)}")
    println("a / b = ${(a / b)}")
    println("a % b = ${(a % b)}")
}