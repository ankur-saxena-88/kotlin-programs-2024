/*
Write a Kotlin program to find the addition of two numbers.
Where, num1 = 45 and num2 = 50
*/

fun main(){

    val num1 = 45
    val num2 = 50

    // calculate sum
    val findSum = num1 + num2

    println("First number is: $num1")
    println("Second number is: $num2")

    // printing result
    println("The sum of $num1 and $num2 is :: $findSum")
}