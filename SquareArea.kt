/*
Write a Kotlin program to calculate the area of a square.
Where, value of the side = 12

Area of a square formula = side * side
*/

fun main(){

    val side = 12.0

    println("Value of the side: $side")

    // calculate area
    val area = side * side

    // print result
    println("Area of a square:: $area")
}