/*
Write a kotlin program to find the multiplication of two numbers.
Where, num1 = 100 and num2 = 200
*/

fun main(){

    val num1 = 100
    val num2 = 200

    // printing value
    println("First number is: $num1")
    println("Second number is: $num2")

    // calculate multiplication
    val result = num1 * num2

    // printing result
    println("The multiplication of $num1 and $num2 is :: $result")
}