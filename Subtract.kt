/*
Kotlin program to find the subtraction of two numbers.
Where, num1 = 65 & num2 = 16
 */

fun findSubtraction(){
    val num1 = 65
    val num2 = 16

    println("First number is: $num1")
    println("Second number is: $num2")

    // calculate subtraction
    val subtract = num1 - num2

    // print result
    println("The subtraction of $num1 and $num2 is :: $subtract")
}

fun main(){
    // calling function
    findSubtraction()
}