/*
Write a Kotlin program to calculate the area of a parallelogram.
Where, base = 10 and height = 8

Formula: Area of a parallelogram = (base * height)
*/

fun main(){

    var base = 10
    var height = 8

    // print values
    println("Value of base: $base")
    println("Value of height: $height")

    // calculate area
    var area = (base * height)

    // print result
    println("Area of a parallelogram:: $area")
}