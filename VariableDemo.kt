fun main(){

    // val for constant value (value cannot be changed)
    val name = "Alice" // this is string variable

    // var for mutable value (value can change)
    var age = 30 // this is number type or integer type variable
    // printing age
    println("Age: $age")
    // changing age value
    age = 35

    // printing values
    println("Name: $name\nAge: $age")
}