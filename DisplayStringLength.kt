/*
Write a Kotlin program to print the length of a string.
Where, string = "Hi, welcome to the world of Kotlin!"
*/

fun main() {

    val string = "Hi, welcome to the world of Kotlin!"

    // print string
    println("The string is: $string")

    // find string length
    val stringLength = string.length

    // print result
    println("The length of the string is: $stringLength")
}